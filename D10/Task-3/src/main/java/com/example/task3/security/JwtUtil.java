package com.example.task3.security;

import io.jsonwebtoken.*;
import io.jsonwebtoken.security.Keys;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;

@Component
public class JwtUtil {
    // Generate a secure key for HMAC-SHA256
    private final Key key = Keys.secretKeyFor(SignatureAlgorithm.HS256);
    
    // Bonus: Token Expiry set to 40 minutes (Task requirement hint)
    private final long EXPIRATION_TIME = 1000 * 60 * 40;

    public String generateToken(String username, String role) {
        return Jwts.builder()
                .setSubject(username)
                .claim("role", role)
                .setIssuedAt(new Date())
                .setExpiration(new Date(System.currentTimeMillis() + EXPIRATION_TIME))
                .signWith(key)
                .compact();
    }

    public Claims extractClaims(String token) {
        return Jwts.parserBuilder().setSigningKey(key).build().parseClaimsJws(token).getBody();
    }

    public boolean isTokenValid(String token) {
        try {
            extractClaims(token);
            return true;
        } catch (JwtException e) {
            return false; // Token is expired or invalid
        }
    }
}
